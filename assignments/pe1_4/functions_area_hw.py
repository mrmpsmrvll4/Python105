# area of a circle
def circle(radius):
    area = 3.14 * radius * radius
    print("The area of the circle is " + str(area) + " square units")
    
# area of a square
def square(side):
    area = side * side
    print("The area of the square is " + str(area) + " square units")

# test
circle(8)
square(7)