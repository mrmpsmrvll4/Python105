# Addition
sum = 5 + 10
print("\nAddition: ")
print(sum)

# Subtraction
diff = 9 - 3
print("\nSubtraction: ")
print(diff)

# Multiplication
product = 2 * 5
print("\nMultiplication: ")
print(product)

# Division
quotient = 12 / 4
print("\nDivision: ")
print(quotient)

# Integer Division
integer_div = 13 // 4
print("\nInteger Division: ")
print(integer_div)

# Modulus
remainder = 9 % 4
print("\nModulus: ")
print(remainder)

# Exponentiation
exp = 2 ** 2
print("\nExponentiation:")
print(exp)